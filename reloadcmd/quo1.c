#include "shell.h"

char	save_quo(char c)
{
	if (c == '"')
		return (-2);
	if (c == '\'')
		return (-1);
	else
		return (c);
}

char	load_quo(char c)
{
	if (c == -2)
		return ('"');
	if (c == -1)
		return ('\'');
	else
		return (c);
}
